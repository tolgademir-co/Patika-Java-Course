package patika_2_core_apis;

public class MathAPIMain {

    public static void main(String[] args) {

        System.out.println(Math.min(3,7)); // İKİ SAYI ARASINDAKİ EN KÜÇÜK SAYIYI BULUR

        System.out.println(Math.min(18,-7));

        System.out.println(Math.max(18,7)); // İKİ SAYI ARASINDAKİ EN BÜYÜK SAYIYI BULUR

        System.out.println(Math.max(10,-7));

        System.out.println();

        System.out.println(Math.round(123.45)); // 0.50 DEN AZ OLURSA AŞAĞIYA YUVARLAR, DEĞİLSE YUKARIYA YUVARLAR--

        System.out.println(Math.round(123.50)); // İKİ SAYI ARASINDAKİ YUVARLAMA DEĞERİNİ BULUR

        System.out.println();

        System.out.println(Math.pow(5, 2)); // BU METHOD ÜST ALMA KOMUTU

        System.out.println(Math.pow(5, 3)); // BU METHOD 5'İN KARESİNİ ALMAYA YARAR

        System.out.println();

        System.out.println(Math.random()); // BU METHOD RANDOM SAYI ÜRETİR

        System.out.println(Math.random()); // İSTEDİĞİMİZ SAYI ARALIĞINDA BİR RANDOM SAYI ÜRETİR..

    }
}
